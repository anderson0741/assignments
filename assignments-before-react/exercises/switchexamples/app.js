let today = "Tuesday";

switch(today){
    case "Sunday":
        console.log("wake up at 2pm!");
        break;
    case "Monday":
        console.log("wake up at 6am!");
        break;
    case "Tuesday":
        console.log("wake up at 7am!");
        break;
    case "Wednesday":
        console.log("wake up at 8am!");
        break;
    case "Thursday":
        console.log("wake up at 9am!");
        break;
    case "Friday":
        console.log("wake up at 10am!");
        break;
    case "Saturday":
        console.log("wake up at 11am!");
        break;
    default:
        console.log("invalid day")
}